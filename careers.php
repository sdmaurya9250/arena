  <h3>Graphic Designer</h3>

<p>Graphic designers create visual concepts, using computer softwares like Adobe Photoshop, Illustrator, Corel Draw etc.,  to communicate ideas that inspire, inform, and captivate consumers. They develop the overall layout and production design for various applications such as for advertisements, brochures, magazines, and corporate reports.</p>
<h3>Web Graphic Designer</h3>

<p>Web graphic designers create determine the layout, font color, font type, logos, pictures and other visual and verbal aspects of a website. First, a Web graphic designer determines what the employer wants out of the product. This includes what message is being sent out and what audience is being targeted. They then create sketches and samples of potential layouts for the website, before developing the finished product.
Web graphic designers have to possess a keen eye for detail. The ability to express creative ideas is crucial. Understanding new fads and trends is necessary. A Web graphic designer has to understand a target audience and how best to reach that audience through artistic work. This career requires a lot of discipline since a Web graphic designer has to start projects, budget time and meet the necessary deadlines placed upon them by an employer.
Illustrator

An illustrator uses creative skills in art and design to communicate a story, message or idea. Illustrators produce still drawings for use in advertisements, books, magazines, packaging, greetings cards and newspapers. It is essential to prepare a sound portfolio of work that can be used to demonstrate abilities.</p>

<h3>HTML Programmers</h3>

<p>HTML programmers write code for web sites and web-based applications using the Hyper Text Markup Language (HTML). They are hired by all kinds of companies to help them grow their web-based products and properties. HTML programmers code sites, update sites, write technical documentation and stay on top of new web-based technologies.</p>

<h3>UX/UI Designers </h3>

<p>UX/UI Designers design Web-based interfaces such as sites, applications, and games. In order to do this effectively, they must consider all aspects of the user experience. Who are the site's target customers, and what is the goal of the interface? What is the "user workflow" expected for the interface? How can text and images be used to communicate clearly and create a specific experience.
<h3>Content Developer</h3>

<p>Content developers are those involved in the development, preparation and editing of contents online for online marketing and other front end web development activities.</p>
<h3>Flash Animators</h3>

<p>Flash animators are involved in creating .swf files with animated program in the website. They create visual graphics, animation, etc., in the website for prompting the marketing campaign and bringing a new life to the website. These visual graphics and animation include cartoons, illustrations, web campaigns, marketing campaigns, etc. Flash animators usually work in collaboration with graphic designers, gaming and entertainment companies, web hosting companies, or marketing firms and are responsible for creating original and creative flash animation as per the company's requirements.</p>

  <h3>Visual Fx Artist</h3>
<p>Visual effects artists are multimedia artists that are responsible for much of the post-production work in the television and film industry. The visual effects artist ensures that all visual effects are woven together seamlessly. Visual effects artists are continuing to become more specialized as the film and television industries embrace the newest in technology.</p>
<h3>Compositor</h3>
<p>Compositors construct the final image by combining layers of previously-created material, including computer animation, special effects, graphics, 2D animation, live action and static background plates</p>
<h3>Concept Artist</h3>
<p>Concept Artists produce the illustrations that help Production Designers realise their vision and also make sure their drawings are accurate and clear and adapting them as necessary during filming.</p>
<h3>Digital Preparation (Paint/Prep) Artist</h3>
<p>Digitally preparing the plate for the Compositing Department to layer in CG, digital matte painting, graphics or other photographed elements, sometimes using green screen, or effects photography.</p>
<h3>Match Move Artist</h3>
<p>Match Move Artists translate and  imitate the camera movements in live action shots and match those movements in 3D computer animations
Lighting Technical Director or Lighter
Ensures there is consistency in lighting, colour balance and mood between the various elements of a shot or scene, makes sure the computer-generated imagery looks photorealistic to match the live action and also add the lighting that creates atmosphere, and adds realism, tone and depth to a scene</p>
<h3>Producer (VFX)</h3>
<p>Responsible for the overall running of a project, from staffing to scheduling and budget.</p>
<h3>Roto Artist</h3>
<p>Roto Artists trace the areas of live action frames where computer graphics will overlap or interact with live image and also create clear areas (mattes) within the frame to allow all elements of the scene to be layered convincingly by the Compositor</p>

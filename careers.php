<h3>Character Rigger</h3>

<p>Character riggers are experts at manipulating the geometry of characters in their interaction with environments. Character riggers accomplish this through a thorough understanding of physics and anatomy, operating systems such as UNIX, and software such as Autodesk Maya, Motion Builder, 3D Studio Max, and XSI. </p>

<h3>Animation Director</h3>

<p>Animation directors recruit and coordinate animation teams. They lead each team from the beginning to the end of any given animated production. Animation directors have a tremendous amount of experience in the art and animation industry. Because of they have advanced skills and years of experience in the field, animation directors are among the most sought after professionals in the animation industry. <p>

<h3>Game Designer</h3>
<p>Game designers perform a number of tasks as a part of the design process. They design user interfaces, create storylines and characters, and role-play mechanics and gameplay prototypes. Game designers collaborate with artists to fine tune the visual style for games. They map out missions, puzzles, and other challenges for players to face during game play. While most game designers work in gaming studios, some work at advertising and design firms, web design firms, multimedia companies, mobile technology firms, and software development firms. They may hold titles such as lead designer, game developer, game designer, interactive designer, lead mobile game designer, lead game developer, senior game designer, and many others.</p>

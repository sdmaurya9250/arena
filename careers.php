  <h3>2D- Animator</h3>
  <p>2-D Animation may sound old-fashioned in a day and age when computers are everywhere and 3-D animation has taken Hollywood by storm, but 2-D animation continues to be an expanding and popular medium -- especially since classical 2-D skills are the foundation for most 3-D work. 2-D animators develop their skills through life drawing, composition, and perspective courses -- studying proportion, line of action, structure, and basic anatomy -- while working in areas as varied as animation, character design, clean-up, doping, modeling, slugging, and storyboarding.</p>
  <h3>3D- Animator</h3>
  <p>3-D animators are occupied with many of the same considerations at their 2-D counterparts -- computers don't get rid of the need for skills in life drawing, concept drawing, composition, character design, etc. 3-D animators will also deal more extensively with modeling, texturing, and lighting in a 3-D environment, often with the use of such software tools and packages as PhotoShop, SoftImage, Alias/Wavefront, Maya, and others.</p>
  <h3>Story-board Artist</h3>
  <p>The Storyboard Artist interprets scripts to create storyboards. This usually means planning shots, visualizing the story before drawing it and being careful to maintain continuity among the shots. Starting out on this career track as an assistant, you'll typically start out by doing clean-up and revisions, eventually working up to preparing some parts of the story board under supervision. This work involves a lot of cutting and pasting, drawing and quick sketching, perspective and composition, and perhaps most importantly, story development and interpretation.</p>
  <h3>LayOut Artist</h3>
  <p>The Layout Artist creates the foundation for the animation by rendering background layouts for each scene usually referring to storyboards and additional research materials. These layouts don't appear in the final production, but are critical for the positioning and perspective of the animation. Layouts are usually done with graphite pencil on punched animation paper; the aim is to provide a stage in which the animators will animate their characters and effects, as well as a blueprint or underdrawing, to be rendered in color by the Background Painters.</p>
  <h3>InBetweener</h3>
  <p>Most artists enter the world of animation by starting as Inbetweeners -- the artists that help the animators and animation assistants complete the action of a scene. It may not sound like much, but it's an important step where you'll learn the basics of animation. An inbetween is one of the transition drawings between two extreme drawings -- the key drawings that distill the essence of an wow action. The inbetweens fill in the action between these key drawings. You'll usually work in a team and learn to imitate the animator's drawings and line quality.</p>
  
  <h3>Animator</h3>
  <p>3D Modeler Jobs
3D modelers create characters and environments for video games and 3D movies as well as images and modeling for websites, graphic designs, animation, film effects, simulations, broadcast design, special effects, characters and props for film, television effects, CD-Rom design, and location-based entertainment. These multi-talented professionals also create images/models for geologists, architects, scientists, engineers, healthcare agencies, and more.
3D modelers work in film and video production studios, game design companies, advertising and graphic design firms, web design firms, software companies, architecture firms, laboratories (both science and medical), colleges and universities, product design firms, and manufacturing firms (retail, home, tools, etc.). Modelers also work in aerospace and for environmental agencies, automotive companies, retail firms, government agencies, crime labs (forensics), interior design firms, building technology firms, real estate companies, and many others.
